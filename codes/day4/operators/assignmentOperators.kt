fun main(args: Array<String>) {
    var a = 10
    var b = 20

    a = a+b
    println("value of a after (a = a+b) : $a")

    a = a-b
    println("value of a after (a = a-b) : $a")

    a = a*b
    println("value of a after (a = a*b) : $a")

    a = a/b
    println("value of a after (a = a/b) : $a")

    a = a%b
    println("value of a after (a = a%b) : $a")
}